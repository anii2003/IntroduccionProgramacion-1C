public class Invertido25 {
    public static void main(String[] args) {
        // Declaración e inicialización de variables
        int i, n;
        i = 25; n = 0;
        
        System.out.println("Programa que imprima números desde el 25 al 0");
        // Proceso
        
        do{
            System.out.println(i);
            i = i - 1;
       }while(i >= n);
    }
    
}
